package conditionalprogram;

public class ProgramIf {

	public static void main(String[] args) {
		
		int i=10;
		
		if(i>10) {
			System.out.println("Greater");
		}
		if(i==10) {
			System.out.println("Equals");
		}
		if(i<=10) {
			System.out.println("Less than");
		}
		else {
			System.out.println("Exit if");
		}
		
	}
	
	

}
